use serde::{Deserialize, Serialize};

#[derive(Debug, Serialize, Deserialize)]
pub struct Location {
    #[serde(rename = "id")]
    id: i32,

    #[serde(rename = "name")]
    name: String,

    #[serde(rename = "isCountry")]
    is_country: bool,
}

impl Location {
    pub fn id(&self) -> i32 {
        self.id
    }
    pub fn name(&self) -> &str {
        &self.name
    }
    pub fn is_country(&self) -> bool {
        self.is_country
    }
}

